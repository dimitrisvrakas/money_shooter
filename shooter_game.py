from pygame import *          # Εισάγουμε όλες τις εντολές της βιβλιοθήκης Pygame, για να δημιουργήσουμε το παιχνίδι.
from random import randint    # Εισάγουμε τη randint(), για να δημιουργούμε τυχαίους ακέραιους αριθμούς.


# ====================================================
# ΓΡΑΜΜΑΤΟΣΕΙΡΕΣ ΚΑΙ ΜΗΝΥΜΑΤΑ
# ====================================================

font.init()   # Ενεργοποιούμε το σύστημα γραμματοσειρών του Pygame.

font1 = font.SysFont("Arial", 80)   # Δημιουργούμε μια μεγάλη γραμματοσειρά μεγέθους 80 για τα μηνύματα νίκης και ήττας.

win = font1.render('YOU WIN!', True, (255, 255, 255))   # Δημιουργούμε το μήνυμα νίκης με λευκό χρώμα.

lose = font1.render('YOU LOSE!', True, (180, 0, 0))   # Δημιουργούμε το μήνυμα ήττας με σκούρο κόκκινο χρώμα.

font2 = font.SysFont("Arial", 36)   # Δημιουργούμε μικρότερη γραμματοσειρά μεγέθους 36 για το σκορ.


# ====================================================
# ΜΟΥΣΙΚΗ ΚΑΙ ΗΧΟΙ
# ====================================================

mixer.init()   # Ενεργοποιούμε το σύστημα ήχου του Pygame.

mixer.music.load('crowd.mp3')
mixer.music.set_volume(0.25)
mixer.music.play(-1)
fire_sound = mixer.Sound('lefta-sound.mp3')   # Φορτώνουμε τον ήχο που θα ακούγεται όταν πυροβολεί ο παίκτης.


# ====================================================
# ΕΙΚΟΝΕΣ ΤΟΥ ΠΑΙΧΝΙΔΙΟΥ
# ====================================================

img_back = "stadio.webp"   # Αποθηκεύουμε το όνομα της εικόνας που θα χρησιμοποιηθεί ως φόντο.

img_bullet = "lefta.png"   # Αποθηκεύουμε το όνομα της εικόνας της σφαίρας.

img_hero = "messi.png"   # Αποθηκεύουμε το όνομα της εικόνας του διαστημοπλοίου.

img_enemy = "tung.png"   # Αποθηκεύουμε το όνομα της εικόνας του εχθρικού UFO.


# ====================================================
# ΜΕΤΑΒΛΗΤΕΣ ΤΟΥ ΠΑΙΧΝΙΔΙΟΥ
# ====================================================

score = 0   # Μετρά πόσα εχθρικά διαστημόπλοια έχει καταστρέψει ο παίκτης.

goal = 10   # Ο παίκτης πρέπει να καταστρέψει 10 εχθρούς για να κερδίσει.

lost = 0   # Μετρά πόσοι εχθροί πέρασαν από το κάτω μέρος της οθόνης.

max_lost = 3  # Αν ο παίκτης χάσει 3 εχθρούς, τότε χάνει το παιχνίδι.


# ====================================================
# ΒΑΣΙΚΗ ΚΛΑΣΗ ΤΩΝ ΑΝΤΙΚΕΙΜΕΝΩΝ
# ====================================================

# Η GameSprite είναι η γονική κλάση.
# Περιέχει κοινά χαρακτηριστικά που χρειάζονται ο παίκτης, οι εχθροί και οι σφαίρες.
class GameSprite(sprite.Sprite):

   # Η __init__ εκτελείται αυτόματα όταν δημιουργείται ένα νέο αντικείμενο.
   def __init__(self, player_image, player_x, player_y, size_x, size_y, player_speed):

       # Καλούμε τον constructor της έτοιμης κλάσης Sprite του Pygame.
       # Έτσι το αντικείμενό μας αποκτά τις βασικές λειτουργίες ενός sprite.
       sprite.Sprite.__init__(self)

       # Φορτώνουμε την εικόνα του αντικειμένου και της δίνουμε το επιθυμητό πλάτος και ύψος.
       self.image = transform.scale(image.load(player_image), (size_x, size_y))

       # Αποθηκεύουμε την ταχύτητα του αντικειμένου.
       self.speed = player_speed

       # Δημιουργούμε ένα αόρατο ορθογώνιο γύρω από την εικόνα.
       # Το rect χρησιμοποιείται για τη θέση και για τον έλεγχο συγκρούσεων.
       self.rect = self.image.get_rect()

       # Ορίζουμε την αρχική οριζόντια θέση του αντικειμένου.
       self.rect.x = player_x

       # Ορίζουμε την αρχική κατακόρυφη θέση του αντικειμένου.
       self.rect.y = player_y

   # Η reset() σχεδιάζει το αντικείμενο μέσα στο παράθυρο.
   def reset(self):

       # Εμφανίζουμε την εικόνα του αντικειμένου στη θέση x και y.
       window.blit(self.image, (self.rect.x, self.rect.y))


# ====================================================
# ΚΛΑΣΗ ΤΟΥ ΠΑΙΚΤΗ
# ====================================================

# Η Player κληρονομεί όλα τα χαρακτηριστικά και τις μεθόδους της GameSprite.
class Player(GameSprite):

   def __init__(self, player_image, player_x, player_y, size_x, size_y, player_speed):
       super().__init__(player_image, player_x, player_y, size_x, size_y, player_speed)
       self.last_shot = 0
       self.reload_time = 300

   # Η update() ελέγχει την κίνηση του παίκτη.
   def update(self):

       # Ελέγχουμε ποια πλήκτρα είναι πατημένα αυτή τη στιγμή.
       keys = key.get_pressed()

       # Ελέγχουμε αν πατιέται το αριστερό βελάκι και ο παίκτης δεν είναι στο αριστερό άκρο.
       if keys[K_LEFT] and self.rect.x > 5:

           # Μειώνουμε τη θέση x, άρα το διαστημόπλοιο κινείται προς τα αριστερά.
           self.rect.x -= self.speed

       # Ελέγχουμε αν πατιέται το δεξί βελάκι και ο παίκτης δεν είναι στο δεξί άκρο.
       if keys[K_RIGHT] and self.rect.x < win_width - 80:

           # Αυξάνουμε τη θέση x, άρα το διαστημόπλοιο κινείται προς τα δεξιά.
           self.rect.x += self.speed

   # Η fire() δημιουργεί μία καινούρια σφαίρα.
   def fire(self):
       now = time.get_ticks()
       if now - self.last_shot >= self.reload_time:
           self.last_shot = now
           bullet = Bullet(img_bullet, self.rect.centerx, self.rect.top, 40, 40, -15)
           bullets.add(bullet)


# ====================================================
# ΚΛΑΣΗ ΤΩΝ ΕΧΘΡΩΝ
# ====================================================

# Η Enemy κληρονομεί από τη GameSprite.
class Enemy(GameSprite):

   # Η update() ελέγχει την κίνηση του εχθρού.
   def update(self):

       # Αυξάνουμε τη θέση y, άρα ο εχθρός κινείται προς τα κάτω.
       self.rect.y += self.speed

       # Δηλώνουμε ότι θέλουμε να αλλάξουμε τη μεταβλητή lost,
       # η οποία έχει δημιουργηθεί έξω από τη μέθοδο.
       global lost

       # Ελέγχουμε αν ο εχθρός πέρασε από το κάτω μέρος του παραθύρου.
       if self.rect.y > win_height:

           # Μεταφέρουμε τον εχθρό σε μία νέα τυχαία οριζόντια θέση.
           self.rect.x = randint(80, win_width - 80)

           # Μεταφέρουμε τον εχθρό ξανά στην κορυφή της οθόνης.
           self.rect.y = 0

           # Αυξάνουμε τον αριθμό των εχθρών που χάθηκαν κατά 1.
           lost = lost + 1


# ====================================================
# ΚΛΑΣΗ ΤΩΝ ΣΦΑΙΡΩΝ
# ====================================================

# Η Bullet κληρονομεί από τη GameSprite.
class Bullet(GameSprite):

   # Η update() ελέγχει την κίνηση της σφαίρας.
   def update(self):

       # Προσθέτουμε την ταχύτητα στη θέση y.
       # Επειδή η ταχύτητα είναι αρνητική, η σφαίρα κινείται προς τα πάνω.
       self.rect.y += self.speed

       # Ελέγχουμε αν η σφαίρα βγήκε από το πάνω μέρος της οθόνης.
       if self.rect.y < 0:

           # Διαγράφουμε τη σφαίρα από όλες τις ομάδες, επειδή δεν φαίνεται πλέον.
           self.kill()


# ====================================================
# ΔΗΜΙΟΥΡΓΙΑ ΤΟΥ ΠΑΡΑΘΥΡΟΥ
# ====================================================

win_width = 700   # Ορίζουμε το πλάτος του παραθύρου σε 700 pixels.

win_height = 500   # Ορίζουμε το ύψος του παραθύρου σε 500 pixels.

display.set_caption("Shooter")   # Ορίζουμε τον τίτλο του παραθύρου.

window = display.set_mode((win_width, win_height))   # Δημιουργούμε το παράθυρο του παιχνιδιού.

# Φορτώνουμε το φόντο και το προσαρμόζουμε στο μέγεθος του παραθύρου.
background = transform.scale(image.load(img_back), (win_width, win_height))


# ====================================================
# ΔΗΜΙΟΥΡΓΙΑ ΤΟΥ ΠΑΙΚΤΗ
# ====================================================

# Δημιουργούμε το διαστημόπλοιο του παίκτη.
# Δίνουμε εικόνα, θέση x, θέση y, πλάτος, ύψος και ταχύτητα.
ship = Player(img_hero, 20, win_height - 160, 110, 160, 10)


# ====================================================
# ΔΗΜΙΟΥΡΓΙΑ ΤΩΝ ΕΧΘΡΩΝ
# ====================================================

# Δημιουργούμε μία ομάδα που θα περιέχει όλους τους εχθρούς.
monsters = sprite.Group()

# Η επανάληψη εκτελείται 5 φορές, άρα δημιουργούνται 5 εχθροί.
for i in range(1, 6):

   # Δημιουργούμε έναν εχθρό με τυχαία θέση και τυχαία ταχύτητα.
   monster = Enemy(
       img_enemy,
       randint(80, win_width - 80),
       -40,
       120,
       120,
       randint(1, 5)
   )

   # Προσθέτουμε τον νέο εχθρό στην ομάδα monsters.
   monsters.add(monster)


# ====================================================
# ΔΗΜΙΟΥΡΓΙΑ ΟΜΑΔΑΣ ΣΦΑΙΡΩΝ
# ====================================================

# Δημιουργούμε μία άδεια ομάδα που θα περιέχει όλες τις σφαίρες.
bullets = sprite.Group()


# ====================================================
# ΜΕΤΑΒΛΗΤΕΣ ΛΕΙΤΟΥΡΓΙΑΣ ΤΟΥ ΠΑΙΧΝΙΔΙΟΥ
# ====================================================

# Η finish δείχνει αν έχει τελειώσει ένας γύρος του παιχνιδιού.
finish = False

# Η run ελέγχει αν συνεχίζεται το βασικό game loop.
run = True


# ====================================================
# ΒΑΣΙΚΟ GAME LOOP
# ====================================================

# Όσο η run είναι True, το παιχνίδι συνεχίζει να λειτουργεί.
while run:

   # Παίρνουμε όλα τα γεγονότα, όπως πάτημα πλήκτρων και κλείσιμο παραθύρου.
   for e in event.get():

       # Ελέγχουμε αν ο χρήστης πάτησε το Χ του παραθύρου.
       if e.type == QUIT:

           # Κάνουμε τη run False, ώστε να σταματήσει το game loop.
           run = False

       # Ελέγχουμε αν πατήθηκε κάποιο πλήκτρο.
       elif e.type == KEYDOWN:

           # Ελέγχουμε αν το πλήκτρο που πατήθηκε είναι το Space.
           if e.key == K_SPACE:

               # Παίζουμε τον ήχο πυροβολισμού.
               fire_sound.play()

               # Καλούμε τη fire(), ώστε να δημιουργηθεί μία καινούρια σφαίρα.
               ship.fire()


   # Ελέγχουμε αν ο γύρος του παιχνιδιού δεν έχει τελειώσει.
   if not finish:

       # Σχεδιάζουμε το φόντο στη θέση 0, 0.
       window.blit(background, (0, 0))


       # Ενημερώνουμε τη θέση του διαστημοπλοίου σύμφωνα με τα βελάκια.
       ship.update()

       # Ενημερώνουμε τη θέση όλων των εχθρών.
       monsters.update()

       # Ενημερώνουμε τη θέση όλων των σφαιρών.
       bullets.update()


       # Σχεδιάζουμε το διαστημόπλοιο στη νέα του θέση.
       ship.reset()

       # Σχεδιάζουμε όλους τους εχθρούς.
       monsters.draw(window)

       # Σχεδιάζουμε όλες τις σφαίρες.
       bullets.draw(window)


       # Ελέγχουμε αν κάποια σφαίρα συγκρούστηκε με κάποιον εχθρό.
       # Το πρώτο True διαγράφει τον εχθρό και το δεύτερο True διαγράφει τη σφαίρα.
       collides = sprite.groupcollide(monsters, bullets, True, True)

       # Η επανάληψη εκτελείται μία φορά για κάθε εχθρό που χτυπήθηκε.
       for c in collides:

           # Αυξάνουμε το σκορ κατά 1.
           score = score + 1

           # Δημιουργούμε έναν νέο εχθρό, ώστε να αντικαταστήσει αυτόν που καταστράφηκε.
           monster = Enemy(
               img_enemy,     # Η εικόνα του εχθρού.
               randint(80, win_width - 80),     # Τυχαία οριζόντια θέση.
               -40,     # Αρχική θέση πάνω από την οθόνη.
               120,     # Πλάτος του εχθρού.
               120,     # Ύψος του εχθρού.
               randint(1, 5)     # Τυχαία ταχύτητα.
           )

           # Προσθέτουμε τον καινούριο εχθρό στην ομάδα monsters.
           monsters.add(monster)


       # Ελέγχουμε αν ο παίκτης συγκρούστηκε με έναν εχθρό
       # ή αν χάθηκαν περισσότεροι εχθροί από το επιτρεπτό όριο.
       if sprite.spritecollide(ship, monsters, False) or lost >= max_lost:

           # Δηλώνουμε ότι ο γύρος τελείωσε.
           finish = True

           # Εμφανίζουμε το μήνυμα ήττας περίπου στο κέντρο της οθόνης.
           window.blit(lose, (200, 200))


       # Ελέγχουμε αν το σκορ έφτασε ή ξεπέρασε τον στόχο.
       if score >= goal:

           # Δηλώνουμε ότι ο γύρος τελείωσε.
           finish = True

           # Εμφανίζουμε το μήνυμα νίκης περίπου στο κέντρο της οθόνης.
           window.blit(win, (200, 200))


       # Μετατρέπουμε το σκορ σε εικόνα κειμένου με λευκό χρώμα.
       text = font2.render("Score: " + str(score), True, (255, 255, 255))

       # Εμφανίζουμε το σκορ στην επάνω αριστερή πλευρά της οθόνης.
       window.blit(text, (10, 20))


       # Μετατρέπουμε τον αριθμό των χαμένων εχθρών σε εικόνα κειμένου.
       text_lose = font2.render("Missed: " + str(lost), True, (255, 255, 255))

       # Εμφανίζουμε τον αριθμό των χαμένων εχθρών κάτω από το σκορ.
       window.blit(text_lose, (10, 50))


       # Ενημερώνουμε την οθόνη, ώστε να εμφανιστούν όλες οι αλλαγές.
       display.update()


   # Αν η finish είναι True, σημαίνει ότι ο παίκτης κέρδισε ή έχασε.
   else:

       # Κάνουμε τη finish ξανά False, ώστε να ξεκινήσει νέος γύρος.
       finish = False

       # Μηδενίζουμε το σκορ.
       score = 0

       # Μηδενίζουμε τον αριθμό των χαμένων εχθρών.
       lost = 0

       # Εξετάζουμε μία-μία όλες τις σφαίρες της ομάδας bullets.
       for b in bullets:

           # Διαγράφουμε κάθε σφαίρα από το παιχνίδι.
           b.kill()

       # Εξετάζουμε έναν-έναν όλους τους εχθρούς της ομάδας monsters.
       for m in monsters:

           # Διαγράφουμε κάθε εχθρό από το παιχνίδι.
           m.kill()


       # Περιμένουμε 3 δευτερόλεπτα πριν ξεκινήσει ο νέος γύρος.
       time.delay(3000)


       # Η επανάληψη εκτελείται 5 φορές για να δημιουργηθούν 5 νέοι εχθροί.
       for i in range(1, 6):

           # Δημιουργούμε έναν νέο εχθρό με τυχαία θέση και ταχύτητα.
           monster = Enemy(
               img_enemy,                       # Η εικόνα του εχθρού.
               randint(80, win_width - 80),     # Τυχαία οριζόντια θέση.
               -40,                             # Αρχική θέση πάνω από την οθόνη.
               120,                             # Πλάτος του εχθρού.
               120,                             # Ύψος του εχθρού.
               randint(1, 5)                    # Τυχαία ταχύτητα.
           )

           # Προσθέτουμε τον νέο εχθρό στην ομάδα monsters.
           monsters.add(monster)


   # Περιμένουμε 50 χιλιοστά του δευτερολέπτου πριν από την επόμενη επανάληψη.
   # Αυτό εμποδίζει το παιχνίδι να τρέχει υπερβολικά γρήγορα.
   time.delay(50)